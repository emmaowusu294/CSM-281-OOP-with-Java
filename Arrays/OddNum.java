package Arrays;
public class OddNum {
    public static void main(String[] args)  {
        // Printing odd numbers between 0 and 10
        for (int i = 0; i <= 20; i++)
        {
         if (i == 10) break;     // The loop stops if the i is 10
          {
            System.out.println(i + 1); // Printing the odd numbers
            i++; // Increasing to keep them odd
         }
    }
    }
}

